#include <unistd.h>

void	ft_print_program_name(char *str)
{
	int	x;

	x = 0;
	while (str[x] != '\0')
	{
		write(1, &str[x], 1);
		x++;
	}
	write(1, "\n", 1);
}

int	main(int argc, char *argv[])
{
	ft_print_program_name(argv[argc - 1]);
}
