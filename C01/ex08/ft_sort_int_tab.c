void	ft_sort_int_tab(int *tab, int size)
{
	int	x;
	int	y;

	y = 0;
	while (y < (size - 1))
	{
		if (tab[y] > tab[y + 1])
		{
			x = tab[y];
			tab[y] = tab[y + 1];
			tab[y + 1] = x;
			y = 0;
		}
		else
		{
			y++;
		}
	}
}
